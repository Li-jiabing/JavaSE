package com.company.day01_02_03;

public class ConstTest01 {

    public static void main(String[] args) {

        /**
         * 关于字面值：
         *    字面值：
         *    - 10、100
         *    - "abc"
         *    - true
         *    字面符就是数据
         *    字面值是java源程序的造成部分之一。包括标识符和关键字他们都是java源程序的组成部分
         *    数据在现实世界当中是分门别累的，所以数据在计算机编程与阿言当中也是有类型的：【数据类型】
         *    -10，100   属于整数型字面值
         *    -3.14      属于浮点型字面值
         *    -true，false  属于布尔型字面值
         *    -"abc" "中国人" 属于字符串型字面值
         *    -'a','人'       属于字符型字面值
         *    注意：
         *         java语言当中所有的字符串型字面值必须使用双引号扩起来，双引号是半角
         *         java语言当中所有的字符型字面值必须使用单引号扩起来，单引号是半角
         *
         */

        System.out.println("abc");
        System.out.println("你最近过的怎么样");
        System.out.println(10);
        System.out.println(100);
        System.out.println(false);
        System.out.println("abc");
    }
}
